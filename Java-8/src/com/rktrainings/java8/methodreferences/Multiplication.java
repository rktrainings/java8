public class Multiplication {
	public static int multiply(int a, int b) {
		return a * b;
	}
	
	public static int division(int a, int b) {
		return a / b;
	}
	
	public static String fullName(String firstName, String lastName) {
		return firstName+" "+lastName;
	}
}
